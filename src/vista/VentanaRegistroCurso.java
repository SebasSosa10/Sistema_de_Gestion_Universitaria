    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (CamposVacios()) {
                String codigo = txtCodigo.getText();
                Programa programa = (Programa) cbxPrograma.getSelectedItem();
                Materia materia = (Materia) cbxMateria.getSelectedItem();
                Docente docente = (Docente) cbxDocente.getSelectedItem();
                String periodo = txtLlenarPeriodo.getText();
                NumJornada jornada = (NumJornada) cbxJornada.getSelectedItem();
                Curso curso = new Curso(codigo, programa, materia, docente, periodo, jornada);
                controlador.guardarCurso(curso);
                JOptionPane.showMessageDialog(null, "Se guardo el curso");
                actualizarTabla(docente);
                limpiarCurso();
                llenarCurso();
            } else {
                JOptionPane.showMessageDialog(null, "Favor no dejar Campos VACIOS");
            }
        } catch (NoSeGuardoElCursoExepcion | YaExisteUnCursoGuardadoConEstasMismasCaracteristicasExepcion | SoloPuedeHaberUnCursoConEsaJornadaExepcion exp) {
            JOptionPane.showMessageDialog(null, exp.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

