    private void actualizarTabla(Docente docente) {
        listaCurso = new Lista<>();
        for (int i = 0; i < controlador.getListaCurso().size(); i++) {
            if (docente.getCodigo().equals(controlador.getListaCurso().get(i).getDocente().getCodigo())) {
                listaCurso.add(controlador.getListaCurso().get(i));
            }
        }
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"CODIGO", "DOCENTE", "JORNADA", "MATERIA",
            "PERIODO"},
                listaCurso.size());
        tblCurso.setModel(modelo);
        TableModel modeloDatos = tblCurso.getModel();
        for (int i = 0; i < listaCurso.size(); i++) {
            Curso aux = listaCurso.get(i);
            modeloDatos.setValueAt(aux.getCodigoCurso(), i, 0);
            modeloDatos.setValueAt(aux.getDocente(), i, 1);
            modeloDatos.setValueAt(aux.getJornada(), i, 2);
            modeloDatos.setValueAt(aux.getMateria(), i, 3);
            modeloDatos.setValueAt(aux.getPeriodo(), i, 4);
        }
    }

    private void actualizarTablaEstudi(Curso curso) {
        listaestudiante = new Lista<>();
        for (int i = 0; i < controlador.getListaCurso().size(); i++) {
            for (int j = 0; j < controlador.getListaCurso().get(i).getListaEstudiates().size(); j++) {
                if (curso.getCodigoCurso().equals(controlador.getListaCurso().get(i).getCodigoCurso())) {
                    listaestudiante.add(controlador.getListaCurso().get(i).getListaEstudiates().get(j));
                }
            }
        }
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"NOMBRE", "CODIGO", "PROGRAMA"},
                listaestudiante.size());
        tblCursoEstudiante.setModel(modelo);
        TableModel modeloDatos = tblCursoEstudiante.getModel();
        for (int i = 0; i < listaestudiante.size(); i++) {
            Estudiante aux = listaestudiante.get(i);
            modeloDatos.setValueAt(aux.getNombre(), i, 0);
            modeloDatos.setValueAt(aux.getCodigo(), i, 1);
            modeloDatos.setValueAt(aux.getPrograma(), i, 2);
        }
    }

    private void llenarPrograma() {
        cbxPrograma.removeAllItems();
        IList<Programa> listaPrograma = controlador.getListaPrograma();
        for (int i = 0; i < listaPrograma.size(); i++) {
            cbxPrograma.addItem(listaPrograma.get(i));
        }
    }

    private void llenarPeriodo() {
        txtLlenarPeriodo.setText(String.valueOf(controlador.obtenerPeriodoActual()));
    }

    private void llenarMateria(Programa programa) {
        cbxMateria.removeAllItems();
        IList<Materia> listaMateria = programa.getListaMateria();
        for (int i = 0; i < listaMateria.size(); i++) {
            cbxMateria.addItem(listaMateria.get(i));
        }
    }

    private void llenarJornada() {
        cbxJornada.addItem(NumJornada.DIA);
        cbxJornada.addItem(NumJornada.NOCHE);
    }

    private void llenarDocente() {
        cbxDocente.removeAllItems();
        IList<Persona> listaDocente = controlador.getListaPersona();
        for (int i = 0; i < listaDocente.size(); i++) {
            if (listaDocente.get(i).getRol().equals(NumRol.DOCENTE)) {
                cbxDocente.addItem((Docente) listaDocente.get(i));
            }
        }
    }

    private void llenarHorario(Curso curso) {
        cbxHorario.removeAllItems();
        if (curso != null) {
            IList<Horario> listaHorario = curso.getListaHorario();
            for (int i = 0; i < listaHorario.size(); i++) {
                cbxHorario.addItem(listaHorario.get(i));
            }
        }
    }

    private void llenarCurso() {
        cbxCurso.removeAllItems();
        IList<Curso> listaCurso = controlador.getListaCurso();
        for (int i = 0; i < listaCurso.size(); i++) {
            cbxCurso.addItem(listaCurso.get(i));
        }
    }

    private void llenarEstudiante() {
        cbxEstudiante.removeAllItems();
        IList<Persona> listaEstudiante = controlador.getListaPersona();
        for (int i = 0; i < listaEstudiante.size(); i++) {
            if (listaEstudiante.get(i).getRol().equals(NumRol.ESTUDIANTE)) {
                cbxEstudiante.addItem((Estudiante) listaEstudiante.get(i));
            }
        }
    }

    private void limpiarCurso() {
        txtCodigo.setText("");
        cbxJornada.setSelectedIndex(-1);
    }

    private void limpiarCursoEst() {
        cbxEstudiante.setSelectedIndex(-1);
    }

    public boolean CamposVaciosEst() {
        if (cbxCurso.getSelectedIndex() == -1 || cbxEstudiante.getSelectedIndex() == -1 || cbxHorario.getSelectedIndex() == -1) {
            return false;
        } else {
            return true;
        }
    }

    public boolean CamposVacios() {
        if (txtCodigo.getText().isEmpty() || cbxJornada.getSelectedIndex() == -1) {
            return false;
        } else {
            return true;
        }
    }

